export const get = async (
  id: UniqueIdentifier,
): Promise<Result<Project, NotFoundError>> => {
  "use cache";
  cacheTag("project", id.toString());

  const result = await list();
  if (!result.success) return result;

  const project = result.data.find((project) => project.id === id);

  if (!project)
    return {
      success: false,
      error: new NotFoundError(`Project of id: ${id} not found`),
    };

  return {
    success: true,
    data: project,
  };
};
